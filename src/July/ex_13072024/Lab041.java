package July.ex_13072024;

public class Lab041 {
    public static void main(String[] args) {
        // compound assignment operators
        int a = 10;
        // +=, _=, *=, /=, %=

        a+= 10; // same as a+10
        a-= 10; // same as a-10
        a/= 10; // same as a/10
        a*= 10; // same as a*10

    }
}
