package ex_13072024;

public class Lab043 {
    public static void main(String[] args) {
        // Relational operators -> Boolean
        // >, <, >=, <=, ==, !=( ! = ) - true or false
        int age_Vinay_Krishna = 29;
        int age_Pramodh = 35;
        boolean result = age_Pramodh > age_Vinay_Krishna;
        boolean result1 = age_Pramodh < age_Vinay_Krishna;
        boolean result2 = age_Pramodh <= age_Vinay_Krishna;
        boolean result3 = age_Pramodh >= age_Vinay_Krishna;
        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);

    }
}
